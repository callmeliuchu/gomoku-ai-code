#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "$0")" && pwd)"
PYTHON_BIN="${PYTHON_BIN:-python}"

BOARD_SIZE="${BOARD_SIZE:-15}"
WIN_LENGTH="${WIN_LENGTH:-5}"
CHANNELS="${CHANNELS:-320}"
CONV_LAYERS="${CONV_LAYERS:-12}"
DEVICE="${DEVICE:-auto}"
SEED="${SEED:-42}"

DATASET="${DATASET:-$ROOT_DIR/heuristic_dataset_15x15_5.npz}"
PRETRAIN_CHECKPOINT="${PRETRAIN_CHECKPOINT:-$ROOT_DIR/gomoku_rule_pretrain_15x15_5.pt}"
FINETUNE_CHECKPOINT="${FINETUNE_CHECKPOINT:-$ROOT_DIR/gomoku_rule_rl_15x15_5.pt}"

GENERATE_GAMES="${GENERATE_GAMES:-12000}"
GENERATE_RANDOM_OPENING_MOVES="${GENERATE_RANDOM_OPENING_MOVES:-2}"
POLICY_SMOOTHING="${POLICY_SMOOTHING:-0.05}"
GENERATE_LOG_EVERY_GAMES="${GENERATE_LOG_EVERY_GAMES:-200}"

PRETRAIN_STEPS="${PRETRAIN_STEPS:-12000}"
PRETRAIN_BATCH_SIZE="${PRETRAIN_BATCH_SIZE:-256}"
PRETRAIN_LR="${PRETRAIN_LR:-3e-4}"
PRETRAIN_WEIGHT_DECAY="${PRETRAIN_WEIGHT_DECAY:-1e-4}"
PRETRAIN_EVAL_EVERY="${PRETRAIN_EVAL_EVERY:-500}"
PRETRAIN_EVAL_AGENT="${PRETRAIN_EVAL_AGENT:-policy}"
PRETRAIN_EVAL_MCTS_SIMS="${PRETRAIN_EVAL_MCTS_SIMS:-96}"
PRETRAIN_SAVE_EVERY="${PRETRAIN_SAVE_EVERY:-1000}"
PRETRAIN_LOG_EVERY_STEPS="${PRETRAIN_LOG_EVERY_STEPS:-100}"

FINETUNE_ITERATIONS="${FINETUNE_ITERATIONS:-3000}"
GAMES_PER_ITER="${GAMES_PER_ITER:-64}"
PPO_EPOCHS="${PPO_EPOCHS:-8}"
MINIBATCH_SIZE="${MINIBATCH_SIZE:-256}"
FINETUNE_LR="${FINETUNE_LR:-3e-4}"
FINETUNE_WEIGHT_DECAY="${FINETUNE_WEIGHT_DECAY:-1e-4}"
GAMMA="${GAMMA:-0.99}"
CLIP_EPS="${CLIP_EPS:-0.2}"
ENTROPY_COEF="${ENTROPY_COEF:-0.01}"
VALUE_COEF="${VALUE_COEF:-1.0}"
MAX_GRAD_NORM="${MAX_GRAD_NORM:-1.0}"
FINETUNE_EVAL_EVERY="${FINETUNE_EVAL_EVERY:-10}"
EVAL_GAMES="${EVAL_GAMES:-20}"
EVAL_HEURISTIC_GAMES="${EVAL_HEURISTIC_GAMES:-8}"
EVAL_TRACE_GAMES="${EVAL_TRACE_GAMES:-1}"
EVAL_TRACE_MAX_MOVES="${EVAL_TRACE_MAX_MOVES:-20}"
FINETUNE_LOG_EVERY_GAMES="${FINETUNE_LOG_EVERY_GAMES:-8}"
FINETUNE_SAVE_EVERY="${FINETUNE_SAVE_EVERY:-10}"
FINETUNE_RANDOM_OPENING_MOVES="${FINETUNE_RANDOM_OPENING_MOVES:-2}"
EARLY_STOP_LOSS="${EARLY_STOP_LOSS:-0.0}"
EARLY_STOP_PATIENCE="${EARLY_STOP_PATIENCE:-0}"
EARLY_STOP_MIN_ITERATIONS="${EARLY_STOP_MIN_ITERATIONS:-0}"

CMD=(
  "$PYTHON_BIN" "$ROOT_DIR/gomoku_rule_rl.py" pipeline
  --board-size "$BOARD_SIZE"
  --win-length "$WIN_LENGTH"
  --channels "$CHANNELS"
  --conv-layers "$CONV_LAYERS"
  --device "$DEVICE"
  --seed "$SEED"
  --dataset "$DATASET"
  --generate-games "$GENERATE_GAMES"
  --generate-random-opening-moves "$GENERATE_RANDOM_OPENING_MOVES"
  --policy-smoothing "$POLICY_SMOOTHING"
  --generate-log-every-games "$GENERATE_LOG_EVERY_GAMES"
  --pretrain-checkpoint "$PRETRAIN_CHECKPOINT"
  --pretrain-steps "$PRETRAIN_STEPS"
  --pretrain-batch-size "$PRETRAIN_BATCH_SIZE"
  --pretrain-lr "$PRETRAIN_LR"
  --pretrain-weight-decay "$PRETRAIN_WEIGHT_DECAY"
  --pretrain-eval-every "$PRETRAIN_EVAL_EVERY"
  --pretrain-eval-agent "$PRETRAIN_EVAL_AGENT"
  --pretrain-eval-mcts-sims "$PRETRAIN_EVAL_MCTS_SIMS"
  --pretrain-save-every "$PRETRAIN_SAVE_EVERY"
  --pretrain-log-every-steps "$PRETRAIN_LOG_EVERY_STEPS"
  --finetune-checkpoint "$FINETUNE_CHECKPOINT"
  --finetune-iterations "$FINETUNE_ITERATIONS"
  --games-per-iter "$GAMES_PER_ITER"
  --ppo-epochs "$PPO_EPOCHS"
  --minibatch-size "$MINIBATCH_SIZE"
  --finetune-lr "$FINETUNE_LR"
  --finetune-weight-decay "$FINETUNE_WEIGHT_DECAY"
  --gamma "$GAMMA"
  --clip-eps "$CLIP_EPS"
  --entropy-coef "$ENTROPY_COEF"
  --value-coef "$VALUE_COEF"
  --max-grad-norm "$MAX_GRAD_NORM"
  --finetune-eval-every "$FINETUNE_EVAL_EVERY"
  --eval-games "$EVAL_GAMES"
  --eval-heuristic-games "$EVAL_HEURISTIC_GAMES"
  --eval-trace-games "$EVAL_TRACE_GAMES"
  --eval-trace-max-moves "$EVAL_TRACE_MAX_MOVES"
  --finetune-log-every-games "$FINETUNE_LOG_EVERY_GAMES"
  --finetune-save-every "$FINETUNE_SAVE_EVERY"
  --finetune-random-opening-moves "$FINETUNE_RANDOM_OPENING_MOVES"
  --early-stop-loss "$EARLY_STOP_LOSS"
  --early-stop-patience "$EARLY_STOP_PATIENCE"
  --early-stop-min-iterations "$EARLY_STOP_MIN_ITERATIONS"
)

if [[ -n "${PRETRAIN_INIT_CHECKPOINT:-}" ]]; then
  CMD+=(--pretrain-init-checkpoint "$PRETRAIN_INIT_CHECKPOINT")
fi
if [[ "${SYMMETRY_AUGMENT:-1}" == "0" ]]; then
  CMD+=(--no-symmetry-augment)
fi

printf 'Running command:\n%s\n' "${CMD[*]}"
exec "${CMD[@]}"
